// export function add that takes in two numbers and returns their added value
export function add(x: number, y: number) {
  return x + y;
}

// export function subtract that takes in two numbers and returns the subtracted value
export function subtract(x: number, y: number) {
  return x - y;
}

// export function multiply that takes in two numbers and returns the product
export function multiply(x: number, y: number) {
  return x * y;
}